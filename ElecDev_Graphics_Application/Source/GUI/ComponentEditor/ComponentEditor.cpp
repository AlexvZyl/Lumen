void ComponentEditor::render() 
{
	// Place editor at correct position.
	ImGui::SetNextWindowPos(m_guiState->popUpPosition);
	// Editor menu.
	ImGui::Begin("Comoponent Editor", &m_guiState->componentEditor, ImGuiWindowFlags_NoDocking 
																	);
	ImGui::End();
}

