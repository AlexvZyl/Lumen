// Class include.
#include "frameBufferObject.h"

//----------------------------------------------------------------------------------------------------------------------
// Functions. 
//----------------------------------------------------------------------------------------------------------------------

// Constructor.
FrameBufferObject::FrameBufferObject() 
{
	GLCall(glGenFramebuffers(1, &m_rendererID));
}

// Destructor.
FrameBufferObject::~FrameBufferObject()
{
	
}

// Bind the FBO.
void FrameBufferObject::bind() 
{
	GLCall(glBindFramebuffer(GL_FRAMEBUFFER, m_rendererID));
}

